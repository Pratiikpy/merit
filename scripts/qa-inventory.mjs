// QA element+copy inventory extractor (TEST-PLAN §15).
// Loads every public page in a real browser and enumerates EVERYTHING a human/agent can see or touch:
// every heading, button, link, input, dynamic mount point, image, and visible text string — plus a
// console-error/overflow/sentinel-leak gate per page. Output: TEST-PLAN-INVENTORY.md (the walk-this-and-
// nothing-is-missed checklist). Run: QA_BASE=https://merit-ecru.vercel.app node scripts/qa-inventory.mjs
import { chromium } from "playwright";
import fs from "node:fs";

const BASE = process.env.QA_BASE || "https://merit-ecru.vercel.app";
const PAGES = [
  { slug: "home", path: "/" },
  { slug: "break", path: "/break.html" },
  { slug: "honesty", path: "/honesty.html" },
  { slug: "benchmark", path: "/benchmark.html" },
  { slug: "onboard", path: "/onboard.html" },
  { slug: "passport", path: "/passport.html" },
  { slug: "brandkit", path: "/brandkit" },
];

const browser = await chromium.launch();
const out = [
  `# Merit — Element & Copy Inventory (auto-generated)`,
  ``,
  `Source: **${BASE}** · generated by \`scripts/qa-inventory.mjs\`. Every heading, control, link, mount point, image, and visible string on every public page. **Walk this to guarantee no element or word is missed.** Regenerate any time the UI changes.`,
];

for (const p of PAGES) {
  const ctx = await browser.newContext({ viewport: { width: 1280, height: 900 } });
  const page = await ctx.newPage();
  const errors = [];
  page.on("console", (m) => { if (m.type() === "error") errors.push(m.text().slice(0, 160)); });
  page.on("pageerror", (e) => errors.push("pageerror: " + String(e).slice(0, 160)));
  page.on("requestfailed", (r) => errors.push("reqfail: " + (r.failure()?.errorText) + " " + r.url().slice(0, 100)));

  let status = 0;
  try {
    const resp = await page.goto(BASE + p.path, { waitUntil: "networkidle", timeout: 45000 });
    status = resp ? resp.status() : 0;
  } catch {
    try { const resp = await page.goto(BASE + p.path, { waitUntil: "domcontentloaded", timeout: 45000 }); status = resp ? resp.status() : 0; }
    catch { status = -1; }
  }
  await page.waitForTimeout(2500);

  const data = await page.evaluate(() => {
    const vis = (el) => { const s = getComputedStyle(el); const r = el.getBoundingClientRect(); return s.display !== "none" && s.visibility !== "hidden" && (r.width > 0 || r.height > 0); };
    const txt = (el) => (el.textContent || "").replace(/\s+/g, " ").trim();
    const q = (sel) => Array.from(document.querySelectorAll(sel));
    return {
      title: document.title,
      description: (document.querySelector('meta[name="description"]') || {}).content || "",
      headings: q("h1,h2,h3,h4").filter(vis).map((h) => `${h.tagName} · ${txt(h)}`),
      buttons: q("button").filter(vis).map((b) => txt(b) || `(icon-button id=${b.id || b.getAttribute("aria-label") || "?"})`),
      links: q("a").filter(vis).map((a) => `${txt(a) || "(no-text)"} → ${a.getAttribute("href")}`),
      inputs: q("input,textarea,select").map((i) => `<${i.tagName.toLowerCase()}> id=${i.id || "-"} type=${i.type || "-"} placeholder="${i.placeholder || ""}" aria-label="${i.getAttribute("aria-label") || ""}"`),
      mounts: q("[id]").map((e) => `#${e.id} <${e.tagName.toLowerCase()}>`),
      images: q("img").map((im) => `${im.getAttribute("src")} alt="${im.alt || ""}"${im.naturalWidth === 0 ? " ⚠BROKEN" : ""}`),
      textLines: (document.body.innerText || "").split("\n").map((s) => s.trim()).filter((s) => s.length > 1),
      sentinels: ["NaN", "undefined", "Infinity", "$NaN", "[object Object]"].filter((s) => (document.body.innerText || "").includes(s)),
      overflow: document.documentElement.scrollWidth > window.innerWidth + 1,
    };
  }).catch((e) => ({ error: String(e) }));

  out.push(`\n---\n\n## ${p.slug} — \`${p.path}\` (HTTP ${status})`);
  if (data.error) { out.push(`\n> extraction failed: ${data.error}`); await ctx.close(); continue; }
  const lines = [...new Set(data.textLines)];
  out.push(`- **title:** ${data.title}`);
  out.push(`- **meta description:** ${data.description}`);
  out.push(`- **console/network errors:** ${errors.length ? "⚠ " + errors.length + " — " + errors.slice(0, 5).join(" | ") : "none ✓"}`);
  out.push(`- **horizontal overflow:** ${data.overflow ? "⚠ YES" : "no ✓"} · **sentinel leaks:** ${data.sentinels.length ? "⚠ " + data.sentinels.join(",") : "none ✓"}`);
  out.push(`\n### Headings (${data.headings.length})\n` + data.headings.map((h) => `- ${h}`).join("\n"));
  out.push(`\n### Buttons (${data.buttons.length}) — click/verify each\n` + data.buttons.map((b) => `- [ ] \`${b}\``).join("\n"));
  out.push(`\n### Links (${data.links.length}) — each resolves\n` + data.links.map((l) => `- [ ] ${l}`).join("\n"));
  out.push(`\n### Inputs / controls (${data.inputs.length})\n` + data.inputs.map((i) => `- [ ] ${i}`).join("\n"));
  out.push(`\n### Dynamic mount points / ids (${data.mounts.length})\n` + data.mounts.map((m) => `- ${m}`).join("\n"));
  out.push(`\n### Images (${data.images.length})\n` + (data.images.length ? data.images.map((i) => `- ${i}`).join("\n") : "- (none)"));
  out.push(`\n### Every visible text string (${lines.length}) — proof-read each\n` + lines.map((l) => `- [ ] ${l}`).join("\n"));
  await ctx.close();
}

await browser.close();
fs.writeFileSync("TEST-PLAN-INVENTORY.md", out.join("\n") + "\n");
console.log("wrote TEST-PLAN-INVENTORY.md");
